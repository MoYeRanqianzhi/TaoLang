// ============================================================================
// taoc — TaoLang 编译器库根模块
// ============================================================================
// 声明编译器的所有子模块，作为库 crate 的入口。
// 各模块职责：
//   - error:   错误类型与诊断报告
//   - lexer:   词法分析（源代码 → Token 流）
//   - parser:  语法分析（Token 流 → AST）
//   - codegen: 代码生成（AST → LLVM IR → 目标文件）
//   - driver:  编译流水线编排
// ============================================================================

/// 错误类型与诊断报告模块
pub mod error;

/// 词法分析模块：将源代码文本拆分为 Token 流
pub mod lexer;

/// 语法分析模块：将 Token 流构建为抽象语法树（AST）
pub mod parser;

/// 代码生成模块：将 AST 翻译为 LLVM IR 并输出目标文件
pub mod codegen;

/// 编译驱动模块：编排完整编译流水线
pub mod driver;
