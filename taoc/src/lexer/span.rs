// ============================================================================
// span.rs — 源码位置信息
// ============================================================================
// Span 表示源代码中的一个连续区域，用于错误报告和诊断信息中的
// 精确位置定位。每个 Token 和 AST 节点都携带一个 Span。
// ============================================================================

/// 源码中的一个连续区域
///
/// 记录了一段代码在源文件中的精确位置，包括字节偏移量和行列号。
/// 用于错误报告时精确指出问题代码的位置。
///
/// # 示例
/// ```text
/// fn main {
/// ^^^^^^^    Span { start: 0, end: 7, line: 1, col: 1 }
/// ```
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub struct Span {
    /// 源文件标识符（支持多文件编译时区分不同文件）
    pub file_id: u32,
    /// 该区域第一个字符在源文本中的字节偏移量
    pub start: usize,
    /// 该区域最后一个字符之后的字节偏移量（半开区间 [start, end)）
    pub end: usize,
    /// 该区域起始位置的行号（从 1 开始）
    pub line: u32,
    /// 该区域起始位置的列号（从 1 开始）
    pub col: u32,
}

impl Span {
    /// 创建一个新的 Span
    pub fn new(file_id: u32, start: usize, end: usize, line: u32, col: u32) -> Self {
        Self { file_id, start, end, line, col }
    }

    /// 创建一个虚拟 Span（用于编译器内部生成的节点，无对应源码位置）
    pub fn dummy() -> Self {
        Self { file_id: 0, start: 0, end: 0, line: 0, col: 0 }
    }
}
